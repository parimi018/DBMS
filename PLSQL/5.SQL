DECLARE
	ID INVENTORY.PRODUCT_ID%TYPE;
	IQ EXCEPTION;
BEGIN
	SELECT PRODUCT_ID INTO ID FROM INVENTORY WHERE QUANTITY<0;
	IF SQL%FOUND THEN RAISE IQ;
	END IF;
	EXCEPTION
		WHEN IQ THEN
			DBMS_OUTPUT.PUT_LINE('INVALID QUANTITY');
			UPDATE INVENTORY SET QUANTITY=0 WHERE PRODUCT_ID=ID;
END;